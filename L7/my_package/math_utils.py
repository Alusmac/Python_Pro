import math


def factorial_math(n: int) -> int:
    """функція, яка обчислює факторіал числа
    factorial number
    """
    return math.factorial(n)


def gcd_math(a: int, b: int) -> int:
    """функція для знаходження найбільшого спільного дільника двох чисел
    Greatest Common Divisor
    """
    return math.gcd(a, b)
